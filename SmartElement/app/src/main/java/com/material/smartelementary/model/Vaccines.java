package com.material.smartelementary.model;

public class Vaccines {

    public String hpv, dpt, pol, inf, tif, mmr;

    public Vaccines() {

    }

    public void setHpv(String hpv) {
        this.hpv = hpv;
    }

    public void setDpt(String dpt) {
        this.dpt = dpt;
    }

    public void setPol(String pol) {
        this.pol = pol;
    }

    public void setInf(String inf) {
        this.inf = inf;
    }

    public void setTif(String tif) {
        this.tif = tif;
    }

    public void setMmr(String mmr) {
        this.mmr = mmr;
    }

    public String getHpv() {
        return hpv;
    }

    public String getDpt() {
        return dpt;
    }

    public String getPol() {
        return pol;
    }

    public String getInf() {
        return inf;
    }

    public String getTif() {
        return tif;
    }

    public String getMmr() {
        return mmr;
    }
}
